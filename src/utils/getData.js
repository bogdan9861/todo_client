export const getData = () => {
  const date = new Date();

  const days = [
    "Воскресенье",
    "Понедельник",
    "Вторник",
    "Среда",
    "Четверг",
    "Пятница",
    "Суббота",
  ];

  const months = [
    "Января",
    "Февраля",
    "Марта",
    "Апреля",
    "Мая",
    "Июня",
    "Июля",
    "Августа",
    "Сентября",
    "Октября",
    "Ноября",
    "Декабря",
  ];

  const WDay = date.getDay();
  const day = date.getDate();
  const month = date.getMonth();

  return `${days[WDay]}, ${day} ${months[month]}`;
};
